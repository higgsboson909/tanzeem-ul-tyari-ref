export function getMessageBracket(totalMinutesLeft: number, countdownType: 'SEHRI' | 'IFTAR'): string[] {
  const pool = countdownType === 'SEHRI' ? motivationalMessages.sehri : motivationalMessages.iftar;
  if (totalMinutesLeft > 600) return pool.longWait;
  if (totalMinutesLeft > 300) return pool.midDay;
  if (totalMinutesLeft > 60) return pool.almostThere;
  if (totalMinutesLeft > 10) return pool.soClose;
  return pool.finalMinutes;
}

export function getBubbleInterval(totalMinutesLeft: number): number {
  if (totalMinutesLeft > 600) return 120000;
  if (totalMinutesLeft > 300) return 90000;
  if (totalMinutesLeft > 60) return 60000;
  if (totalMinutesLeft > 10) return 30000;
  return 15000;
}
